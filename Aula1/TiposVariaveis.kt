fun main() {
    /**
     * As funções println() a seguir imprimem os valores mínimos e máximos
     * dos tipos primitivos Int, Long, double e Float
     **/
    println("Valor mínimo do INT:${ Int.MIN_VALUE }")
    println("Valor máximo do INT:${ Int.MAX_VALUE }")
    println("Valor mínimo do Long:${ Long.MIN_VALUE }")
    println("Valor máximo do Long:${ Long.MAX_VALUE }")
    println("Valor mínimo do Double:${ Double.MIN_VALUE }")
    println("Valor máximo do Double:${ Double.MAX_VALUE }")
    println("Valor mínimo do Float:${ Float.MIN_VALUE }")
    println("Valor máximo do Float:${ Float.MAX_VALUE }")
}